# Dashboard 顯示引擎
#   使用 ANSI escape code 在固定區域覆寫，不滾動螢幕。
#   佔用固定 8 行，批次全程只更新這 8 行。
# ===========================================================================

class _Dashboard:
    """
    固定 8 行的批次進度 dashboard。
    呼叫 init() 後使用 update() 更新，結束呼叫 finalize()。

    行配置：
      行 0: 標題列
      行 1: 總進度條
      行 2: ETA / 速度
      行 3: 當前分子名稱 + 階段
      行 4: 當前分子內部進度條（perturb / IG）
      行 5: 統計（✓ / ✗ / ⊘）
      行 6: GPU 備忘
      行 7: 空行（緩衝）
    """
    LINES = 8

    def __init__(self):
        self._init_done  = False
        self._last_lines = [""] * self.LINES
        self._lock       = threading.Lock()

    def init(self, n_total: int, output_dir: str):
        self.n_total    = n_total
        self.output_dir = output_dir
        # 印出初始佔位行
        print("\n" * self.LINES, end="")
        self._init_done = True
        self.update(0, 0, 0, 0, 0.0, "", "")

    def _move_up(self, n):
        return f"\033[{n}A"

    def _erase_line(self):
        return "\033[2K\r"

    def update(self, done: int, success: int, skipped: int, failed: int,
               elapsed: float, mol_name: str, stage: str,
               inner_pct: float = -1.0):
        """
        done       : 已處理分子數（含跳過）
        inner_pct  : 當前分子內部進度 0–100，-1 表示不顯示
        """
        if not self._init_done:
            return
        with self._lock:
            n   = self.n_total
            pct = done / n * 100 if n else 0
            bar_w = 30
            filled = int(pct / 100 * bar_w)
            bar = _c(chr(9608) * filled, "green") + _c(chr(9617) * (bar_w - filled), "dim")

            # ETA
            eta_str = ""
            speed   = ""
            if done > 0 and elapsed > 0:
                spm = elapsed / done
                rem = spm * (n - done)
                m, s = divmod(int(rem), 60)
                h, m = divmod(m, 60)
                eta_str = f"ETA {h}h{m:02d}m{s:02d}s" if h else f"ETA {m}m{s:02d}s"
                speed   = f"{spm:.1f}s/mol"

            # 內部進度條
            if inner_pct >= 0:
                iw = 20
                if_filled = int(inner_pct / 100 * iw)
                inner_bar = (_c(chr(9608) * if_filled, "cyan") +
                             _c(chr(9617) * (iw - if_filled), "dim"))
                stage_line = f"  {_c(mol_name[:40], 'yellow')}  [{inner_bar}] {inner_pct:5.1f}%  {_c(stage, 'dim')}"
            else:
                stage_line = (f"  {_c(mol_name[:40], 'yellow')}  "
                              f"{_c(stage, 'dim')}" if mol_name else "")

            lines = [
                f"  {_c('GNN-QSAR 批次計算', 'bold', 'cyan')}  →  {_c(self.output_dir, 'dim')}",
                f"  [{bar}] {pct:5.1f}%  {done}/{n}",
                f"  {_c(eta_str, 'cyan')}  {_c(speed, 'dim')}",
                stage_line,
                f"  {_c('─'*58, 'dim')}",
                (f"  {_c('✓', 'green')} {success}  "
                 f"{_c('✗', 'red')} {failed}  "
                 f"{_c('⊘', 'cyan')} {skipped} 跳過  "
                 f"{'  ' + _c('有失敗，詳見 batch_summary.txt', 'yellow') if failed else ''}"),
                "",
                "",
            ]

            # 上移並覆寫
            out = self._move_up(self.LINES)
            for line in lines:
                out += self._erase_line() + line + "\n"
            sys.stdout.write(out)
            sys.stdout.flush()
            self._last_lines = lines

    def log_error(self, mol_name: str, reason: str):
        """在 dashboard 上方印出錯誤（不破壞 dashboard）。"""
        with self._lock:
            # 先跳到 dashboard 上方
            sys.stdout.write(self._move_up(self.LINES))
            sys.stdout.write(self._erase_line())
            print(_c(f"  ✗ {mol_name}: {reason[:80]}", "red"))
            # 重印 dashboard
            out = ""
            for line in self._last_lines:
                out += self._erase_line() + line + "\n"
            sys.stdout.write(out)
            sys.stdout.flush()

    def _update_stage(self, mol_name: str, stage: str, inner_pct: float):
        """只更新 stage 行（由 method_* 函式呼叫）。"""
        with self._lock:
            iw = 20
            if_filled = int(inner_pct / 100 * iw)
            inner_bar = (_c(chr(9608) * if_filled, "cyan") +
                         _c(chr(9617) * (iw - if_filled), "dim"))
            stage_line = (f"  {_c(mol_name[:40], 'yellow')}  "
                          f"[{inner_bar}] {inner_pct:5.1f}%  {_c(stage, 'dim')}")
            # 只覆寫第 3 行（從底部數 LINES-1-3 行上移）
            up = self.LINES - 3
            out = f"\033[{up}A" + "\033[2K\r" + stage_line + "\n"
            # 重新移回底部
            out += f"\033[{up - 1}B"
            sys.stdout.write(out)
            sys.stdout.flush()
            self._last_lines[3] = stage_line

    def finalize(self):
        """結束後讓游標停在 dashboard 下方。"""
        print()


# 全域 dashboard 實例（main() 使用，方法函式透過此實例回報內部進度）
_DASHBOARD = None


def _dash_update_inner(mol_name: str, stage: str, pct: float):
    """供 method_* 函式呼叫，更新當前分子的內部進度。"""
    if _DASHBOARD is not None:
        # 不知道 done/success 等，只更新 stage 行
        _DASHBOARD._update_stage(mol_name, stage, pct)


