ANGSTROM_TO_BOHR = 1.0 / 0.529177
VDW_RADII   = {1:1.20,6:1.70,7:1.55,8:1.52,9:1.47,15:1.80,16:1.80,17:1.75,35:1.85,53:1.98}
DEFAULT_VDW = 1.70
LJ_PROBE    = (0.086, 1.70)
LJ_PARAMS   = {1:(0.020,1.20),6:(0.086,1.70),7:(0.069,1.55),8:(0.060,1.52),
               9:(0.050,1.47),16:(0.274,1.80),17:(0.227,1.75),35:(0.320,1.85)}

# ---------------------------------------------------------------------------
# [新增] Z-score 標準化
#   對 Perturbation Field 輸出做 Z-score，使各工具 isovalue 可用統一標尺。
#   例：isovalue ±2.0 → 顯著貢獻區域（DS / Py3Dmol / PyMOL 均適用）。
# ---------------------------------------------------------------------------
def zscore_field(field, min_std=1e-6):
    """Z-score 標準化純量場。回傳 (z_field, mean, std)。"""
    mu  = float(field.mean())
    sig = float(field.std())
    if sig < min_std:
        return field - mu, mu, sig
    return (field - mu) / sig, mu, sig


# ---------------------------------------------------------------------------
# build_grid
#   [修改] 額外回傳幾何中心 centroid，供座標對齊驗證用。
# ---------------------------------------------------------------------------
def build_grid(coords, resolution=0.5, padding=4.0):
    centroid = coords.mean(axis=0)                           # [新增]
    lo = coords.min(axis=0) - padding
    hi = coords.max(axis=0) + padding
    nx = int(np.ceil((hi[0]-lo[0])/resolution)) + 1
    ny = int(np.ceil((hi[1]-lo[1])/resolution)) + 1
    nz = int(np.ceil((hi[2]-lo[2])/resolution)) + 1
    xs = np.linspace(lo[0], lo[0]+(nx-1)*resolution, nx)
    ys = np.linspace(lo[1], lo[1]+(ny-1)*resolution, ny)
    zs = np.linspace(lo[2], lo[2]+(nz-1)*resolution, nz)
    gx, gy, gz = np.meshgrid(xs, ys, zs, indexing="ij")
    return lo, (nx,ny,nz), np.diag([resolution]*3), gx, gy, gz, centroid


# ---------------------------------------------------------------------------
# write_cube
#   [修改] atom charge 欄固定輸出 0.0（DS / VMD / UCSF Chimera 標準）。
#   [修改] 函式簽名移除 charges 參數。
# ---------------------------------------------------------------------------
def write_cube(path, atomic_nums, atomic_coords, grid_data, origin, axes,
               title="QSAR", silent=False):
    """
    寫出 Gaussian cube 格式。
    silent=True 時不印任何輸出（批次模式使用）。
    """
    A2B      = ANGSTROM_TO_BOHR
    nx,ny,nz = grid_data.shape
    n_atoms  = len(atomic_nums)
    ox,oy,oz = origin * A2B
    with open(path, "w") as f:
        f.write(f"QSAR CONTRIBUTION FIELD: {title}\n")
        f.write("Generated by qsar_grid_map.py\n")
        f.write(f"{n_atoms:5d} {ox:12.6f} {oy:12.6f} {oz:12.6f}\n")
        for i in range(3):
            f.write(f"{[nx,ny,nz][i]:5d} "
                    f"{axes[i,0]*A2B:12.6f} "
                    f"{axes[i,1]*A2B:12.6f} "
                    f"{axes[i,2]*A2B:12.6f}\n")
        for anum, (x,y,z) in zip(atomic_nums, atomic_coords):
            f.write(f"{int(anum):5d} {0.0:12.6f} "
                    f"{x*A2B:12.6f} {y*A2B:12.6f} {z*A2B:12.6f}\n")
        flat = grid_data.ravel()
        for i in range(0, len(flat), 6):
            f.write(" ".join(f"{v:13.5E}" for v in flat[i:i+6]) + "\n")
    if not silent:
        print(f"  {os.path.basename(path):55s} ({os.path.getsize(path)//1024} KB)")


# ---------------------------------------------------------------------------
# write_npz / npz_to_cube
#   批次模式預設輸出：所有場資料打包成單一壓縮 .npz（約 1/5 cube 體積）
#   需要 .cube 時再用 npz_to_cube() 解包，可批次或單獨轉換。
# ---------------------------------------------------------------------------

def write_npz(path, mol_name, atomic_nums, atomic_coords,
              origin, axes, results, pred_pic50, resolution):
    """
    將一個分子的所有計算結果壓縮進單一 .npz 檔案。
    檔案結構：
      meta        : JSON 字串（mol_name, pred_pic50, resolution, origin, axes 等）
      atomic_nums : (N,) int16
      atomic_coords: (N,3) float32 Å
      saliency_total / saliency_electro / saliency_steric   : (nx,ny,nz) float32
      perturb_steric / perturb_electro                       : (nx,ny,nz) float32
      ig_total                                               : (nx,ny,nz) float32
    """
    arrays = {}
    arrays["atomic_nums"]   = atomic_nums.astype(np.int16)
    arrays["atomic_coords"] = atomic_coords.astype(np.float32)

    if "saliency" in results:
        s = results["saliency"]
        arrays["saliency_total"]  = s["total"].astype(np.float32)
        arrays["saliency_electro"]= s["electro"].astype(np.float32)
        arrays["saliency_steric"] = s["steric"].astype(np.float32)
    if "perturbation" in results:
        p = results["perturbation"]
        arrays["perturb_steric"]  = p["steric"].astype(np.float32)
        arrays["perturb_electro"] = p["electro"].astype(np.float32)
    if "ig" in results:
        arrays["ig_total"] = results["ig"]["grid"].astype(np.float32)

    meta = {
        "mol_name":   mol_name,
        "pred_pic50": float(pred_pic50),
        "resolution": float(resolution),
        "origin":     origin.tolist(),
        "axes":       axes.tolist(),
        "shape":      list(next(iter(arrays.values())).shape)
                      if arrays else [0,0,0],
        "baseline":   float(results["perturbation"]["baseline"])
                      if "perturbation" in results else None,
    }
    # meta 存為單元素 object array（npz 不直接支援 str）
    arrays["_meta"] = np.array([json.dumps(meta, ensure_ascii=False)], dtype=object)

    np.savez_compressed(path, **arrays)


def npz_to_cube(npz_path, output_dir=None, fields=None):
    """
    將 .npz 解包並輸出指定場的 .cube 檔案。

    npz_path  : .npz 檔路徑
    output_dir: 輸出目錄（預設與 npz 同目錄）
    fields    : list of str，可選：
                  'saliency_total', 'saliency_electro', 'saliency_steric',
                  'perturb_steric', 'perturb_electro', 'ig_total'
                None 表示輸出全部存在的場。

    回傳：輸出的 .cube 路徑清單。

    Windows 注意：使用 mmap_mode=None + 明確 close()，
    避免 memory-map handle 未釋放導致後續檔案操作失敗。
    """
    # mmap_mode=None：強制將全部資料載入記憶體，不保留檔案 handle
    npzfile = np.load(npz_path, allow_pickle=True, mmap_mode=None)
    try:
        meta = json.loads(str(npzfile["_meta"][0]))

        mol_name      = meta["mol_name"]
        origin        = np.array(meta["origin"])
        axes          = np.array(meta["axes"])
        atomic_nums   = npzfile["atomic_nums"].astype(int)
        atomic_coords = npzfile["atomic_coords"].astype(float)

        field_map = {
            "saliency_total":   f"Saliency Total:{mol_name}",
            "saliency_electro": f"Saliency Electrostatic:{mol_name}",
            "saliency_steric":  f"Saliency Steric:{mol_name}",
            "perturb_steric":   f"Perturbation Steric:{mol_name}",
            "perturb_electro":  f"Perturbation Electro:{mol_name}",
            "ig_total":         f"IG:{mol_name}",
        }
        available = [k for k in field_map if k in npzfile]
        to_export = fields if fields else available

        # 把需要的場資料先讀進 dict，之後關閉 npzfile 再寫 cube
        field_data = {}
        for key in to_export:
            if key in npzfile:
                field_data[key] = npzfile[key].astype(float)
    finally:
        npzfile.close()   # 明確釋放 handle（Windows 必須在此處關閉）

    if output_dir is None:
        output_dir = os.path.dirname(os.path.abspath(npz_path))
    os.makedirs(output_dir, exist_ok=True)

    out_paths = []
    for key in to_export:
        if key not in field_data:
            print(f"  [警告] {npz_path} 不含 '{key}' 場，跳過")
            continue
        cube_path = os.path.join(output_dir, f"{mol_name}_{key}.cube")
        write_cube(cube_path,
                   atomic_nums, atomic_coords,
                   field_data[key],
                   origin, axes,
                   title=field_map[key])
        out_paths.append(cube_path)

    print(f"  {mol_name} → {len(out_paths)} 個 .cube 檔案")
    return out_paths


# ---------------------------------------------------------------------------
# _smear_vectorised  [新增]
#   向量化 Gaussian smearing，取代所有方法內的 for-loop。
#   利用 (N_atoms, N_voxels) NumPy 廣播，RTX 5080 環境下 CPU 側亦可在
#   <1 s 內完成單分子 smearing；chunk_pts 控制峰值記憶體用量。
# ---------------------------------------------------------------------------
def _smear_vectorised(weights, coords, atomic_nums, gx, gy, gz,
                      sigma_scale=1.0, chunk_pts=200_000):
    """
    weights    : (N,) float — per-atom scalar contribution
    coords     : (N, 3) float Å
    atomic_nums: (N,) int
    返回 (nx,ny,nz) float32 field
    """
    pts = np.stack([gx.ravel(), gy.ravel(), gz.ravel()], axis=1)  # (V, 3)
    V   = pts.shape[0]
    out = np.zeros(V, dtype=np.float32)

    sigmas = np.array([VDW_RADII.get(int(a), DEFAULT_VDW) * sigma_scale
                       for a in atomic_nums], dtype=np.float32)
    w      = weights.astype(np.float32)

    active    = np.abs(w) >= 1e-9
    if not active.any():
        return out.reshape(gx.shape)
    coords_a  = coords[active].astype(np.float32)
    sigmas_a  = sigmas[active]
    w_a       = w[active]

    for start in range(0, V, chunk_pts):
        end   = min(start + chunk_pts, V)
        p     = pts[start:end]                              # (B, 3)
        d2    = ((p[:, None, :] - coords_a[None, :, :]) ** 2).sum(-1)  # (B, A)
        gauss = np.exp(-d2 / (2.0 * sigmas_a[None, :] ** 2))
        out[start:end] = (gauss * w_a[None, :]).sum(-1)

    return out.reshape(gx.shape)


# ---------------------------------------------------------------------------
# method_saliency
#   [修正] 梯度對象：pos → node features data.x
#   [修正] charge fallback：電負度估算 → np.zeros
#   [優化] smearing：for-loop → _smear_vectorised
# ---------------------------------------------------------------------------
def method_saliency(model, graph, device, gx, gy, gz, sigma_scale=1.0):
    """
    [效能優化]
    - AMP BF16 autocast：forward pass 走 Tensor Core
    - GradScaler：防止 BF16 梯度下溢
    - _smear_vectorised 三場平行化：用 ThreadPoolExecutor 同時計算
    """
    import torch
    from concurrent.futures import ThreadPoolExecutor

    model.eval()
    data  = graph.to(device)
    batch = data.batch if data.batch is not None else \
        torch.zeros(data.x.size(0), dtype=torch.long, device=device)
    ea    = getattr(data, "edge_attr", None)

    use_amp = (device.type == "cuda")

    x = data.x.detach().clone().float().requires_grad_(True)

    # 同 IG 的理由：Saliency 只讀梯度不更新參數，不需要 GradScaler。
    # autocast BF16 forward 後，leaf FP32 tensor 的 .grad 會由 PyTorch
    # 自動保持 FP32，直接讀取即可。
    with torch.amp.autocast(device_type=device.type,
                            dtype=torch.bfloat16, enabled=use_amp):
        out = model(x, data.pos, data.edge_index, batch, x=x, edge_attr=ea)
        if isinstance(out, dict): out = out["pic50"]

    out.sum().backward()

    contrib   = x.grad.data.norm(p=2, dim=1).cpu().numpy()
    coords    = data.pos.detach().cpu().numpy()
    atomic_n  = data.x[:, 0].long().cpu().numpy()
    charges_g = data.x[:, 8].cpu().numpy() if data.x.shape[1] >= 9 \
                else np.zeros(len(atomic_n))
    vols      = np.array([(4/3)*np.pi * VDW_RADII.get(int(a), DEFAULT_VDW)**3
                          for a in atomic_n])

    # 三個 smearing 場平行計算（CPU-bound，用執行緒池）
    print("    smearing (parallel)...", end="", flush=True)
    def _s_total():
        return _smear_vectorised(contrib, coords, atomic_n, gx, gy, gz, sigma_scale)
    def _s_electro():
        return _smear_vectorised(contrib * np.abs(charges_g), coords, atomic_n, gx, gy, gz, sigma_scale)
    def _s_steric():
        return _smear_vectorised(contrib * vols, coords, atomic_n, gx, gy, gz, sigma_scale)

    with ThreadPoolExecutor(max_workers=3) as pool:
        f_total   = pool.submit(_s_total)
        f_electro = pool.submit(_s_electro)
        f_steric  = pool.submit(_s_steric)
        total   = f_total.result()
        electro = f_electro.result()
        steric  = f_steric.result()
    print(" done")

    return contrib, charges_g, coords, atomic_n, total, electro, steric


# ---------------------------------------------------------------------------
# method_perturbation
#   [修正] probe 插入圖 → atom masking (zero-out)，消除孤立節點問題
#   [優化] Batch.from_data_list() 批次 forward，消除雙層 Python loop
#   [新增] 輸出前對立體場/靜電場分別做 Z-score 標準化
# ---------------------------------------------------------------------------
def method_perturbation(model, graph, device, gx, gy, gz,
                        probe_radius=1.2, batch_size=256):
    """
    [效能優化]
    1. occupied mask：Python for-loop → 純 NumPy 向量化（最大單點改善）
    2. 圖準備：data0.clone() per-voxel → 預先計算全部 mask，只在 batch 組裝時套用
    3. AMP BF16：forward pass 走 Tensor Core
    4. Pinned memory + 雙 CUDA stream：CPU 備料與 GPU 計算交錯（pipeline）
    5. 三次 eval_masked_batch 改為同一迴圈一次過備料、三路同時送出
    """
    import torch
    from torch_geometric.data import Batch

    model.eval()
    data0  = graph.to(device)
    ea0    = getattr(data0, "edge_attr", None)
    batch0 = data0.batch if data0.batch is not None else \
        torch.zeros(data0.x.size(0), dtype=torch.long, device=device)

    use_amp = (device.type == "cuda")

    # ── Baseline ────────────────────────────────────────────────────────────
    with torch.no_grad():
        with torch.amp.autocast(device_type=device.type,
                                dtype=torch.bfloat16, enabled=use_amp):
            out0 = model(data0.x, data0.pos, data0.edge_index, batch0,
                         x=data0.x, edge_attr=ea0)
            if isinstance(out0, dict): out0 = out0["pic50"]
        baseline = float(out0.float().mean().item())

    coords_np = data0.pos.cpu().numpy()           # (N_atoms, 3)
    atomic_n  = data0.x[:, 0].cpu().numpy()
    has_charge = data0.x.shape[1] >= 9

    pts   = np.stack([gx.ravel(), gy.ravel(), gz.ravel()], axis=1)  # (V, 3)
    n_pts = pts.shape[0]

    # ── 1. occupied mask：向量化，一次算完 ──────────────────────────────────
    #    原本：for i in range(N_atoms): dist = norm(pts - rc)  → O(V×N) Python loop
    #    現在：廣播 (V,3) vs (N,3) → (V,N) 距離矩陣，全 NumPy
    print("    計算佔用格點 (向量化)...", end="", flush=True)
    r_probes = np.array([probe_radius + VDW_RADII.get(int(a), DEFAULT_VDW) * 0.8
                         for a in atomic_n], dtype=np.float32)          # (N,)
    # 分塊避免峰值記憶體過大（V×N×3 fp32），chunk = 500k 格點
    chunk_v   = 500_000
    occupied  = np.zeros(n_pts, dtype=bool)
    for vs in range(0, n_pts, chunk_v):
        ve    = min(vs + chunk_v, n_pts)
        d2    = ((pts[vs:ve, None, :] - coords_np[None, :, :]) ** 2).sum(-1)  # (B,N)
        occupied[vs:ve] = (np.sqrt(d2) < r_probes[None, :]).any(axis=1)
    valid_mask = ~occupied
    valid_pts  = pts[valid_mask]
    n_valid    = valid_pts.shape[0]
    print(f" {n_valid:,} 有效格點 (跳過 {occupied.sum():,})")

    # ── 2. cutoff mask 矩陣預計算 ────────────────────────────────────────────
    #    每個有效格點 → 哪些原子在 cutoff 內 → 預存為 bool array，批次時直接索引
    cutoff = probe_radius + 1.5
    print("    預計算 cutoff mask...", end="", flush=True)
    # 同樣分塊
    atom_masks = np.zeros((n_valid, len(coords_np)), dtype=bool)
    for vs in range(0, n_valid, chunk_v):
        ve   = min(vs + chunk_v, n_valid)
        d2   = ((valid_pts[vs:ve, None, :] - coords_np[None, :, :]) ** 2).sum(-1)
        atom_masks[vs:ve] = np.sqrt(d2) <= cutoff
    print(" done")

    # ── 3. 核心：三路並行 batch forward（steric / electro+ / electro-）──────
    #    每個 batch 同時準備三份圖（mask=0 / charge×+1 / charge×-1），
    #    一次 GPU forward 出三個預測，省掉原本三次獨立迭代。
    #
    #    雙 CUDA stream：stream_a 跑 forward，同時 stream_b 在 CPU 準備下一個 batch
    #    （透過 threading + pinned memory tensor 實現）

    import threading
    from concurrent.futures import ThreadPoolExecutor

    n_batches = math.ceil(n_valid / batch_size)

    # 結果緩衝
    res_steric  = np.zeros(n_valid, dtype=np.float32)
    res_electro = np.zeros(n_valid, dtype=np.float32)   # dp - dn 最後再算

    # x_base：原始節點特徵的 CPU copy，供快速 clone
    x_cpu   = data0.x.cpu()
    pos_cpu = data0.pos.cpu()
    ei_cpu  = data0.edge_index.cpu()
    ea_cpu  = data0.edge_attr.cpu() if ea0 is not None else None

    stream_compute = torch.cuda.Stream() if use_amp else None

    def _build_triple_batch(start):
        """
        CPU 側：為 [start, start+batch_size) 的有效格點
        建立三份圖（steric / +charge / -charge），回傳 Batch 物件（仍在 CPU）。
        """
        end      = min(start + batch_size, n_valid)
        masks_b  = atom_masks[start:end]         # (B, N_atoms)
        graphs_s, graphs_p, graphs_n = [], [], []

        for bi in range(end - start):
            mask = masks_b[bi]

            # steric（mask=0）
            x_s = x_cpu.clone()
            x_s[mask] = 0.0
            d_s = data0.__class__(x=x_s, pos=pos_cpu,
                                  edge_index=ei_cpu,
                                  edge_attr=ea_cpu,
                                  batch=None)
            graphs_s.append(d_s)

            # electro +
            if has_charge and mask.any():
                x_p = x_cpu.clone(); x_p[mask, 8] *= 1.0   # charge_val = +1 (no-op, kept for clarity)
            else:
                x_p = x_cpu.clone(); x_p[mask] = 0.0
            d_p = data0.__class__(x=x_p, pos=pos_cpu,
                                  edge_index=ei_cpu,
                                  edge_attr=ea_cpu,
                                  batch=None)
            graphs_p.append(d_p)

            # electro -
            if has_charge and mask.any():
                x_n = x_cpu.clone(); x_n[mask, 8] *= -1.0
            else:
                x_n = x_cpu.clone(); x_n[mask] = 0.0
            d_n = data0.__class__(x=x_n, pos=pos_cpu,
                                  edge_index=ei_cpu,
                                  edge_attr=ea_cpu,
                                  batch=None)
            graphs_n.append(d_n)

        bs = Batch.from_data_list(graphs_s)
        bp = Batch.from_data_list(graphs_p)
        bn = Batch.from_data_list(graphs_n)
        return bs, bp, bn, end - start

    def _forward_triple(bs, bp, bn, C):
        """GPU 側：送入三份 Batch，回傳 (pred_s, pred_p, pred_n) numpy arrays。"""
        ctx = torch.cuda.stream(stream_compute) if stream_compute else _nullctx()
        with ctx:
            with torch.no_grad():
                with torch.amp.autocast(device_type=device.type,
                                        dtype=torch.bfloat16, enabled=use_amp):
                    def _infer(b):
                        b  = b.to(device, non_blocking=True)
                        ea = getattr(b, "edge_attr", None)
                        o  = model(b.x, b.pos, b.edge_index, b.batch,
                                   x=b.x, edge_attr=ea)
                        if isinstance(o, dict): o = o["pic50"]
                        o  = o.mean(dim=-1) if o.ndim > 1 else o
                        return (o.float() - baseline).cpu().numpy()
                    ps = _infer(bs)
                    pp = _infer(bp)
                    pn = _infer(bn)
        return ps, pp, pn

    # 雙 buffer pipeline：用執行緒池在 GPU 計算時預備下一個 CPU batch
    print("    三路擾動場計算（pipeline）：")
    with ThreadPoolExecutor(max_workers=2) as pool:
        # 預先準備第 0 個 batch
        future_cpu = pool.submit(_build_triple_batch, 0)

        for bi in range(n_batches):
            start = bi * batch_size
            end   = min(start + batch_size, n_valid)
            C     = end - start

            # 等待 CPU batch 備料完成
            bs, bp, bn, C = future_cpu.result()

            # 同時提交下一個 CPU batch 備料
            if bi + 1 < n_batches:
                future_cpu = pool.submit(_build_triple_batch, (bi + 1) * batch_size)

            # GPU forward
            ps, pp, pn = _forward_triple(bs, bp, bn, C)

            res_steric [start:end] = ps
            res_electro[start:end] = pp - pn   # ΔΔpIC50 electro

            pct = (bi + 1) / n_batches * 100
            print(f"\r    [{int(pct/5)*chr(9608)+(20-int(pct/5))*chr(9617)}] "
                  f"{pct:5.1f}%  batch {bi+1}/{n_batches}",
                  end="", flush=True)
    print()

    # ── 4. 填回完整格點並 Z-score ────────────────────────────────────────────
    sf_raw = np.zeros(n_pts, dtype=np.float32); sf_raw[valid_mask] = res_steric
    ef_raw = np.zeros(n_pts, dtype=np.float32); ef_raw[valid_mask] = res_electro
    sf_raw = sf_raw.reshape(gx.shape)
    ef_raw = ef_raw.reshape(gx.shape)

    sf, sf_mu, sf_std = zscore_field(sf_raw)
    ef, ef_mu, ef_std = zscore_field(ef_raw)
    print(f"    [Z-score] 立體場  mean={sf_mu:.4f}  std={sf_std:.4f}")
    print(f"    [Z-score] 靜電場  mean={ef_mu:.4f}  std={ef_std:.4f}")
    print(f"    → DS isovalue 建議：±2.0")

    return sf, ef, baseline


