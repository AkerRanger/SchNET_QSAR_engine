class _AlignedSdfWriter:
    """
    執行緒安全的累積 SDF 寫入器。
    使用 Lock 保護，允許主執行緒與子任務安全地 append。
    """
    def __init__(self, path: str):
        from rdkit.Chem import SDWriter
        self._path   = path
        self._writer = SDWriter(path)
        self._lock   = threading.Lock()
        self._count  = 0

    def write(self, mol):
        with self._lock:
            self._writer.write(mol)
            self._count += 1

    def close(self):
        with self._lock:
            self._writer.close()

    @property
    def count(self):
        with self._lock:
            return self._count


# ===========================================================================
# CPU 多核預處理 Worker
#   在獨立 Process 中平行執行 _prepare_mol（MMFF + graph 建構），
#   結果透過 multiprocessing.Queue 傳回主 process。
#   GPU forward 在主 process 執行（CUDA context 不跨 process）。
#   worker 數建議 = min(16, cpu_count // 2)，避免搶占 GPU thread 資源。
# ===========================================================================

def _worker_fn(task_queue, result_queue, engine_kwargs, source):
    """
    子 process worker：從 task_queue 取 (idx, mol_name, mol_raw, activity)，
    執行 _prepare_mol，把 (idx, mol_name, mol_out_sdf_block, graph_data, label, err)
    放入 result_queue。

    mol_out 以 SDF block string 傳回（跨 process 序列化 RDKit Mol）。
    graph_data 以 dict of numpy arrays 傳回（跨 process 序列化 PyG Data）。
    """
    # 每個 worker process 需要重新初始化 engine
    import sys, os
    sys.path.insert(0, engine_kwargs["model_dir"])
    from gpu_qsar_engine import GpuQsarEngine, DataConfig
    import dataclasses

    data_cfg = DataConfig(**engine_kwargs["data_cfg"])
    engine   = GpuQsarEngine(data_cfg)

    from rdkit import Chem
    from rdkit.Chem import SDWriter
    import io

    while True:
        task = task_queue.get()
        if task is None:   # sentinel
            break
        idx, mol_name, mol_raw_block, is_smiles, activity = task
        try:
            # 還原 mol_raw
            if is_smiles:
                mol_raw = Chem.MolFromSmiles(mol_raw_block)
                src = "smiles"
            else:
                mol_raw = Chem.MolFromMolBlock(mol_raw_block, removeHs=False)
                src = "sdf"

            mol_out, graph, label, err = _prepare_mol(
                mol_name, mol_raw, engine, activity=activity, source=src)

            if mol_out is None:
                result_queue.put((idx, mol_name, None, None, label, err))
                continue

            # 序列化 mol_out → SDF block string
            sio = io.StringIO()
            w   = SDWriter(sio)
            w.write(mol_out)
            w.flush()
            mol_sdf_block = sio.getvalue()

            # 序列化 graph → dict of numpy（避免 PyG Data pickle 問題）
            graph_dict = {
                "x":          graph.x.numpy(),
                "pos":        graph.pos.numpy(),
                "edge_index": graph.edge_index.numpy(),
                "edge_attr":  graph.edge_attr.numpy() if graph.edge_attr is not None else None,
            }
            result_queue.put((idx, mol_name, mol_sdf_block, graph_dict, label, None))

        except Exception as exc:
            result_queue.put((idx, mol_name, None, None,
                              activity or 0.0, traceback.format_exc(limit=3)))


def _graph_from_dict(graph_dict, device):
    """將 worker 傳回的 dict 重建為 PyG Data 物件並移到 device。"""
    import torch
    from torch_geometric.data import Data
    x  = torch.from_numpy(graph_dict["x"]).float()
    pos= torch.from_numpy(graph_dict["pos"]).float()
    ei = torch.from_numpy(graph_dict["edge_index"]).long()
    ea = torch.from_numpy(graph_dict["edge_attr"]).float() \
         if graph_dict["edge_attr"] is not None else None
    return Data(x=x, pos=pos, edge_index=ei, edge_attr=ea).to(device)


def _mol_from_sdf_block(sdf_block):
    """從 SDF block string 重建 RDKit Mol。"""
    from rdkit import Chem
    return Chem.MolFromMolBlock(sdf_block, removeHs=False)


def _mol_to_block(mol, is_smiles, mol_raw):
    """將 mol_raw 序列化為可跨 process 傳輸的格式。"""
    if is_smiles:
        from rdkit import Chem
        return Chem.MolToSmiles(mol_raw)
    else:
        from rdkit.Chem import SDWriter
        import io
        sio = io.StringIO()
        w   = SDWriter(sio)
        w.write(mol_raw)
        w.flush()
        return sio.getvalue()


