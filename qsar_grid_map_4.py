# run_qsar_grid_map  （主流程）
#   [修改] build_grid 回傳值新增 centroid
#   [修改] write_cube 呼叫移除 charges 引數
#   [新增] aligned_mol 參數：若傳入對齊後的 RDKit Mol，自動輸出 _aligned.sdf
# ---------------------------------------------------------------------------
def run_qsar_grid_map(model, graph, device,
                      output_dir="qsar_grid_maps", mol_name="molecule",
                      resolution=0.5, padding=4.0,
                      methods=None, sigma_scale=1.0,
                      n_ig_steps=50, perturb_batch=256,
                      aligned_mol=None,
                      output_mode="npz",   # "npz" | "cube" | "both"
                      silent=False):
    """
    output_mode:
      "npz"  — 只輸出 .npz（預設，批次模式）
      "cube" — 只輸出 .cube
      "both" — 同時輸出 .npz 與 .cube
    silent:
      True  — 不印任何進度（批次模式由外層 dashboard 控制）
      False — 印完整進度（單分子模式）
    """
    import torch
    os.makedirs(output_dir, exist_ok=True)
    base = os.path.join(output_dir, mol_name)
    t0   = time.perf_counter()
    if methods is None: methods = ["saliency", "perturbation", "ig"]

    def _log(msg):
        if not silent: print(msg)

    coords_np = graph.pos.cpu().numpy()
    atomic_n  = graph.x[:, 0].long().cpu().numpy()
    n_atoms   = len(atomic_n)

    # CUDA context 暖機（只在第一次呼叫時實際耗時，之後為 no-op）
    if device.type == "cuda" and not getattr(run_qsar_grid_map, "_warmed_up", False):
        _ = torch.zeros(1, device=device)
        torch.cuda.synchronize()
        run_qsar_grid_map._warmed_up = True

    with torch.no_grad():
        model.eval()
        d  = graph.to(device)
        ea = getattr(d, "edge_attr", None)
        ba = d.batch if d.batch is not None else \
            torch.zeros(n_atoms, dtype=torch.long, device=device)
        o  = model(d.x, d.pos, d.edge_index, ba, x=d.x, edge_attr=ea)
        if isinstance(o, dict): o = o["pic50"]
        pred_pic50 = float(o.mean().item())

    _log(f"\n[QSAR格點圖] {mol_name}  原子:{n_atoms}  pIC50:{pred_pic50:.4f}  "
         f"解析度:{resolution}Å")

    origin, shape, axes, gx, gy, gz, centroid = build_grid(
        coords_np, resolution=resolution, padding=padding)
    nx, ny, nz = shape
    n_pts      = nx * ny * nz
    _log(f"  格點: {nx}×{ny}×{nz} = {n_pts:,}")
    _log(f"  幾何中心: ({centroid[0]:.2f}, {centroid[1]:.2f}, {centroid[2]:.2f}) Å")

    # 對齊 SDF：批次模式由外層 _AlignedSdfWriter 統一寫，單分子模式才在這裡寫
    if aligned_mol is not None and not silent:
        try:
            from rdkit.Chem import SDWriter
            sdf_out = base + "_aligned.sdf"
            w = SDWriter(sdf_out)
            w.write(aligned_mol)
            w.close()
            _log(f"  對齊座標 → {os.path.basename(sdf_out)}")
        except Exception as e:
            _log(f"  [警告] 無法寫出對齊 .sdf：{e}")

    results = {}

    if "saliency" in methods:
        _log("\n[A] Saliency 塗抹...")
        contrib, charges_g, coords_g, an_g, total, electro, steric = method_saliency(
            model, graph, device, gx, gy, gz, sigma_scale)
        results["saliency"] = {"contrib": contrib, "total": total,
                               "electro": electro, "steric": steric}

    if "perturbation" in methods:
        _log("\n[B] 擾動場 (Z-score 標準化)...")
        sg, eg, bl = method_perturbation(
            model, graph, device, gx, gy, gz, batch_size=perturb_batch)
        results["perturbation"] = {"steric": sg, "electro": eg, "baseline": bl}

    if "ig" in methods:
        _log("\n[C] Integrated Gradients...")
        ig_attr, grid_ig = method_integrated_gradients(
            model, graph, device, gx, gy, gz, n_ig_steps, sigma_scale)
        results["ig"] = {"attr": ig_attr, "grid": grid_ig}

    elapsed = time.perf_counter() - t0

    # ── 輸出 ────────────────────────────────────────────────────────────────
    if output_mode in ("npz", "both"):
        write_npz(base + ".npz", mol_name, atomic_n, coords_np,
                  origin, axes, results, pred_pic50, resolution)

    if output_mode in ("cube", "both"):
        if "saliency" in results:
            s = results["saliency"]
            write_cube(base+"_saliency_total.cube",
                       atomic_n, coords_np, s["total"],
                       origin, axes, f"Saliency Total:{mol_name}", silent=silent)
            write_cube(base+"_saliency_electrostatic.cube",
                       atomic_n, coords_np, s["electro"],
                       origin, axes, f"Saliency Electrostatic:{mol_name}", silent=silent)
            write_cube(base+"_saliency_steric.cube",
                       atomic_n, coords_np, s["steric"],
                       origin, axes, f"Saliency Steric:{mol_name}", silent=silent)
        if "perturbation" in results:
            p = results["perturbation"]
            write_cube(base+"_perturbation_steric.cube",
                       atomic_n, coords_np, p["steric"],
                       origin, axes, f"Perturbation Steric:{mol_name}", silent=silent)
            write_cube(base+"_perturbation_electro.cube",
                       atomic_n, coords_np, p["electro"],
                       origin, axes, f"Perturbation Electro:{mol_name}", silent=silent)
        if "ig" in results:
            write_cube(base+"_ig_total.cube",
                       atomic_n, coords_np, results["ig"]["grid"],
                       origin, axes, f"IG:{mol_name}", silent=silent)

    write_report(base+"_qsar_report.txt", mol_name, pred_pic50,
                 n_atoms, n_pts, results, resolution, elapsed)

    if not silent:
        print(f"\n[完成] {elapsed:.1f}s  -> {os.path.abspath(output_dir)}/")
        print("\n  Discovery Studio 建議：")
        if output_mode in ("cube", "both"):
            print(f"  ① 開啟對齊後的分子：{mol_name}_aligned.sdf")
            if "perturbation" in results:
                print(f"  ② {mol_name}_perturbation_steric.cube  正+2.0(綠)/負-2.0(黃)")
                print(f"  ③ {mol_name}_perturbation_electro.cube 正+2.0(藍)/負-2.0(紅)")
            if "saliency" in results:
                print(f"  ④ {mol_name}_saliency_total.cube       Isovalue=max×0.35")
            if "ig" in results:
                print(f"  ⑤ {mol_name}_ig_total.cube             Isovalue=max×0.35")
        else:
            print(f"  ① 開啟 all_aligned.sdf")
            print(f"  ② 用 npz_to_cube('{mol_name}.npz') 解包需要的場")

    return results, pred_pic50


