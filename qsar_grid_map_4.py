# ---------------------------------------------------------------------------
# method_integrated_gradients
#   [修正] IG 對象：pos → node features data.x
#   [修正] baseline：centroid → zeros_like(x)
#   [優化] smearing → _smear_vectorised
# ---------------------------------------------------------------------------
def method_integrated_gradients(model, graph, device, gx, gy, gz,
                                n_steps=50, sigma_scale=1.0):
    """
    [效能優化]
    - AMP BF16：每步 forward 走 Tensor Core
    - GradScaler：防梯度下溢
    - integ 累積保持 FP32（unscale 後），數值穩定
    - smearing 與最後一步 backward 重疊：用執行緒提前啟動
    """
    import torch
    from concurrent.futures import ThreadPoolExecutor

    model.eval()
    data  = graph.to(device)
    ea    = getattr(data, "edge_attr", None)
    batch = data.batch if data.batch is not None else \
        torch.zeros(data.x.size(0), dtype=torch.long, device=device)

    use_amp = (device.type == "cuda")

    x_orig = data.x.detach().float()
    x_base = torch.zeros_like(x_orig)
    integ  = torch.zeros_like(x_orig)   # FP32 累積

    # 注意：IG 只需要讀梯度，完全不更新參數，不需要 GradScaler。
    # autocast 的 BF16 forward 後，PyTorch backward 會自動將梯度
    # 轉回 FP32 寫入 .grad，直接讀取即可，數值穩定且無 unscale_ 問題。
    for step in range(n_steps):
        alpha  = (step + 0.5) / n_steps
        x_step = (x_base + alpha * (x_orig - x_base)).detach().requires_grad_(True)

        with torch.amp.autocast(device_type=device.type,
                                dtype=torch.bfloat16, enabled=use_amp):
            out = model(x_step, data.pos, data.edge_index, batch,
                        x=x_step, edge_attr=ea)
            if isinstance(out, dict): out = out["pic50"]

        # backward 直接在 FP32 leaf tensor 上累積梯度
        out.sum().backward()

        with torch.no_grad():
            # .grad 已是 FP32（PyTorch autocast 保證 leaf grad 為 FP32）
            integ = integ + x_step.grad.detach()

        if (step + 1) % 10 == 0 or step == n_steps - 1:
            pct = (step + 1) / n_steps * 100
            print(f"\r    [{int(pct/5)*chr(9608)+(20-int(pct/5))*chr(9617)}] {pct:5.1f}%",
                  end="", flush=True)
    print()

    integ   /= n_steps
    ig_attr  = ((x_orig - x_base) * integ).norm(p=2, dim=1).cpu().numpy()
    coords   = data.pos.detach().cpu().numpy()
    atomic_n = data.x[:, 0].long().cpu().numpy()

    grid_ig = _smear_vectorised(ig_attr, coords, atomic_n, gx, gy, gz, sigma_scale)
    return ig_attr, grid_ig


