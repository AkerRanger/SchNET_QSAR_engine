_CKPT_FILE = "batch_checkpoint.json"

def _ckpt_load(output_dir: str) -> set:
    """
    載入已完成的 mol_name 集合。若無 checkpoint 檔則回傳空集合。
    """
    path = os.path.join(output_dir, _CKPT_FILE)
    if not os.path.isfile(path):
        return set()
    try:
        with open(path, encoding="utf-8") as fh:
            data = json.load(fh)
        return set(data.get("completed", []))
    except Exception:
        return set()


def _ckpt_save(output_dir: str, completed: set):
    """
    寫出 checkpoint，包含已完成分子名稱清單與時間戳。
    使用「先寫暫存再 rename」確保不產生半寫檔。
    """
    path     = os.path.join(output_dir, _CKPT_FILE)
    tmp_path = path + ".tmp"
    payload  = {
        "completed": sorted(completed),
        "count":     len(completed),
        "updated":   time.strftime("%Y-%m-%d %H:%M:%S"),
    }
    with open(tmp_path, "w", encoding="utf-8") as fh:
        json.dump(payload, fh, ensure_ascii=False, indent=2)
    os.replace(tmp_path, path)


