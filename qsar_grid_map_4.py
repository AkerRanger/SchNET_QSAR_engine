# 3D 構象產生（SMILES → 3D Mol）
# ===========================================================================

def _smiles_to_3d(mol_2d, mol_name: str, n_confs: int = 1, seed: int = 42):
    """
    從 2D RDKit Mol（無 3D）產生 3D 構象並做 MMFF 最佳化。
    回傳 (mol_3d, None) 或 (None, error_str)。

    流程：
      1. AddHs
      2. EmbedMolecule（ETKDGv3，若失敗降級為 ETKDG，再失敗用 random）
      3. MMFFOptimizeMolecule（最多 2000 步）
    """
    from rdkit import Chem
    from rdkit.Chem import AllChem

    try:
        mol = Chem.AddHs(mol_2d)

        params = AllChem.ETKDGv3()
        params.randomSeed  = seed
        params.maxIterations = 1000
        params.numThreads  = 0     # 使用全部 CPU 核心

        ret = AllChem.EmbedMolecule(mol, params)
        if ret == -1:
            # 降級 ETKDG
            params2 = AllChem.EmbedParameters()
            params2.randomSeed = seed
            ret = AllChem.EmbedMolecule(mol, params2)
        if ret == -1:
            # 最後手段：隨機座標
            ret = AllChem.EmbedMolecule(mol, AllChem.ETKDG())
        if ret == -1:
            return None, f"{mol_name}: EmbedMolecule 完全失敗（結構可能太複雜）"

        ff_result = AllChem.MMFFOptimizeMolecule(mol, maxIters=2000)
        # ff_result: 0=已最佳化, 1=未收斂(仍可用), -1=無 FF
        if ff_result == -1:
            # 嘗試 UFF
            uff_result = AllChem.UFFOptimizeMolecule(mol, maxIters=2000)
            if uff_result == -1:
                return None, f"{mol_name}: MMFF 與 UFF 均無法建立力場"

        return mol, None

    except Exception as exc:
        return None, f"{mol_name}: 3D 建構例外 — {exc}"


# ===========================================================================
# 3D 分子前處理（含 MMFF minimization，適用 SDF 與 SMILES 來源）
# ===========================================================================

def _prepare_mol(mol_name: str, mol_raw, engine,
                 activity: float | None = None,
                 source: str = "sdf"):
    """
    輸入原始 Mol（可能是 2D SMILES 來源或已有 3D 的 SDF 分子），
    輸出 (mol_out, graph, label, error_str)。

    source: "sdf" | "smiles"
      - sdf    → 直接呼叫 engine._minimize_mmff
      - smiles → 先做 _smiles_to_3d，再呼叫 engine._minimize_mmff
    """
    from rdkit import Chem

    label = float(activity) if activity is not None else 0.0

    try:
        if source == "smiles":
            mol_3d, err = _smiles_to_3d(mol_raw, mol_name)
            if mol_3d is None:
                return None, None, label, err
        else:
            mol_3d = mol_raw   # SDF 分子已有 3D

        mol_out, _ = engine._minimize_mmff(mol_3d)
        if mol_out is None:
            return None, None, label, f"{mol_name}: engine._minimize_mmff 回傳 None"

        graph = engine.mol_to_graph(mol_out, label=label, smiles="")
        return mol_out, graph, label, None

    except Exception as exc:
        return None, None, label, f"{mol_name}: 前處理例外 — {traceback.format_exc(limit=3)}"


