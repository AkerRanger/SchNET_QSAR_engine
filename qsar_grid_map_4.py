def _sanitize_mol_name(name: str, fallback: str) -> str:
    """
    將分子名稱轉為合法檔名（移除空白與特殊字元）。
    name 為空或全空白時使用 fallback。
    """
    name = (name or "").strip()
    if not name:
        return fallback
    # 保留字母、數字、底線、連字號、點；其餘換底線
    name = re.sub(r"[^\w.\-]", "_", name)
    # 避免 Windows 保留名稱（CON / PRN / AUX / NUL / COM1…COM9 / LPT1…LPT9）
    reserved = re.compile(r"^(CON|PRN|AUX|NUL|COM[1-9]|LPT[1-9])$", re.IGNORECASE)
    if reserved.match(name):
        name = "_" + name
    return name[:120]   # 限長，避免路徑過長


def _load_sdf_batch(sdf_path: str):
    """
    載入 SDF，回傳 list of (mol_name: str, mol: Chem.Mol, activity: float|None)。
    mol_name 取自 _Name property；若為空則用 mol_XXXXX 流水號。
    activity 取自 property 'activity' / 'pIC50' / 'pic50'（大小寫不分）。
    """
    from rdkit import Chem
    suppl = Chem.SDMolSupplier(sdf_path, removeHs=False, sanitize=True)
    records = []
    activity_keys = {"activity", "pic50", "pic_50", "pIC50"}
    for idx, mol in enumerate(suppl):
        if mol is None:
            print(_c(f"  [略過] SDF 第 {idx+1} 個條目解析失敗", "yellow"))
            continue
        raw_name = mol.GetProp("_Name") if mol.HasProp("_Name") else ""
        mol_name = _sanitize_mol_name(raw_name, f"mol_{idx+1:06d}")
        # 嘗試讀取活性值
        activity = None
        for pname in mol.GetPropsAsDict():
            if pname.lower().replace("-","_") in activity_keys:
                try:
                    activity = float(mol.GetProp(pname))
                except (ValueError, TypeError):
                    pass
                break
        records.append((mol_name, mol, activity))
    return records


def _load_csv_batch(csv_path: str, sdf_index: dict | None = None):
    """
    載入 CSV，回傳 list of (mol_name, mol_or_smiles, activity)。

    支援兩種模式：
      A) SMILES 欄位（欄名包含 'smiles'）→ 從 SMILES 建立 RDKit Mol（無 3D）
         後續 pipeline 會自動 EmbedMolecule + MMFF
      B) mol_name 欄位 + sdf_index → 從預載 SDF dict 查找 Mol

    欄名偵測（大小寫不分）：
      名稱欄: 'name' / 'mol_name' / 'id' / 'compound_id' / 'cmpd_id'
      SMILES欄: 'smiles' / 'canonical_smiles' / 'smi'
      活性欄: 'activity' / 'pic50' / 'pic_50' / 'pIC50' / 'value'
    """
    from rdkit import Chem

    name_keys     = {"name","mol_name","id","compound_id","cmpd_id"}
    smiles_keys   = {"smiles","canonical_smiles","smi"}
    activity_keys = {"activity","pic50","pic_50","value"}

    with open(csv_path, newline="", encoding="utf-8-sig") as fh:
        reader = csv.DictReader(fh)
        headers = [h.strip() for h in (reader.fieldnames or [])]

    # 欄名映射（不分大小寫）
    col_name     = next((h for h in headers if h.lower().replace("-","_") in name_keys),     None)
    col_smiles   = next((h for h in headers if h.lower().replace("-","_") in smiles_keys),   None)
    col_activity = next((h for h in headers if h.lower().replace("-","_") in activity_keys), None)

    if col_smiles is None and sdf_index is None:
        raise ValueError(
            "CSV 缺少 SMILES 欄（smiles/smi）且未提供 SDF 索引。\n"
            "  請確認 CSV 包含 smiles 欄，或同時提供 SDF 檔案。"
        )

    records = []
    with open(csv_path, newline="", encoding="utf-8-sig") as fh:
        reader = csv.DictReader(fh)
        for row_idx, row in enumerate(reader):
            raw_name = row.get(col_name, "").strip() if col_name else ""
            mol_name = _sanitize_mol_name(raw_name, f"mol_{row_idx+1:06d}")

            activity = None
            if col_activity:
                try:
                    activity = float(row[col_activity])
                except (ValueError, TypeError):
                    pass

            if col_smiles:
                smi = row[col_smiles].strip()
                if not smi:
                    print(_c(f"  [略過] CSV 第 {row_idx+2} 行：SMILES 為空", "yellow"))
                    continue
                mol = Chem.MolFromSmiles(smi)
                if mol is None:
                    print(_c(f"  [略過] CSV 第 {row_idx+2} 行 ({mol_name})：SMILES 無效", "yellow"))
                    continue
                records.append((mol_name, mol, activity))
            else:
                # 從 SDF 索引查找
                mol = sdf_index.get(mol_name)
                if mol is None:
                    print(_c(f"  [略過] 找不到 SDF 中對應分子：{mol_name}", "yellow"))
                    continue
                records.append((mol_name, mol, activity))

    return records


def _build_sdf_index(sdf_path: str) -> dict:
    """
    預載 SDF 所有分子，建立 {mol_name: mol} 字典，供 CSV 模式查找。
    """
    from rdkit import Chem
    index = {}
    suppl = Chem.SDMolSupplier(sdf_path, removeHs=False, sanitize=True)
    for idx, mol in enumerate(suppl):
        if mol is None:
            continue
        raw = mol.GetProp("_Name") if mol.HasProp("_Name") else ""
        name = _sanitize_mol_name(raw, f"mol_{idx+1:06d}")
        index[name] = mol
    return index


