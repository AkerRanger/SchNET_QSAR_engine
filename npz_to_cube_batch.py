# 進度 Dashboard（固定行覆寫）
# ══════════════════════════════════════════════════════════════════════════════

class _Progress:
    """簡單的單行進度列（不滾動）。"""
    def __init__(self, total: int):
        self.total   = total
        self.done    = 0
        self.success = 0
        self.failed  = 0
        self._lock   = threading.Lock()
        self._t0     = time.perf_counter()

    def update(self, ok: bool):
        with self._lock:
            self.done += 1
            if ok: self.success += 1
            else:  self.failed  += 1
            self._render()

    def _render(self):
        pct    = self.done / self.total * 100
        filled = int(pct / 5)
        bar    = (_c(chr(9608) * filled,        "green") +
                  _c(chr(9617) * (20 - filled), "dim"))
        elapsed = time.perf_counter() - self._t0
        eta     = ""
        if self.done > 0:
            rem = elapsed / self.done * (self.total - self.done)
            m, s = divmod(int(rem), 60)
            eta  = f"  ETA {m}m{s:02d}s"
        line = (f"\r  [{bar}] {pct:5.1f}%  "
                f"{self.done}/{self.total}  "
                f"{_c(f'✓{self.success}','green')}  "
                f"{_c(f'✗{self.failed}','red') if self.failed else ''}"
                f"{eta}   ")
        sys.stdout.write(line)
        sys.stdout.flush()

    def finish(self):
        elapsed = time.perf_counter() - self._t0
        sys.stdout.write("\n")
        sys.stdout.flush()
        return elapsed


