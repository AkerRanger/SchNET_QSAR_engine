"""
npz_to_cube_batch.py
====================
批次將 qsar_grid_maps/ 目錄下所有 .npz 轉換為 .cube 檔案。

用法：
  python npz_to_cube_batch.py                        ← 互動式，詢問目錄
  python npz_to_cube_batch.py --input qsar_grid_maps
  python npz_to_cube_batch.py --input qsar_grid_maps --fields perturb_steric perturb_electro
  python npz_to_cube_batch.py --input qsar_grid_maps --workers 8
"""

import os, sys, gc, json, time, argparse, traceback
import multiprocessing
import threading
import numpy as np

# ── ANSI 顏色 ─────────────────────────────────────────────────────────────────
_C = {
    "reset": "\033[0m","bold":   "\033[1m",
    "cyan":  "\033[96m","green": "\033[92m",
    "yellow":"\033[93m","red":   "\033[91m",
    "dim":   "\033[2m",
}
def _c(text, *keys):
    if not sys.stdout.isatty(): return str(text)
    return "".join(_C.get(k,"") for k in keys) + str(text) + _C["reset"]

def _hr(char="─", width=62):
    return _c(char * width, "dim")

# ── 可選場名稱與說明 ──────────────────────────────────────────────────────────
ALL_FIELDS = [
    "saliency_total",
    "saliency_electro",
    "saliency_steric",
    "perturb_steric",
    "perturb_electro",
    "ig_total",
]

FIELD_LABELS = {
    "saliency_total":   "Saliency Total",
    "saliency_electro": "Saliency Electrostatic",
    "saliency_steric":  "Saliency Steric",
    "perturb_steric":   "Perturbation Steric  (DS: Green+2/Yellow-2)",
    "perturb_electro":  "Perturbation Electro (DS: Blue+2/Red-2)",
    "ig_total":         "Integrated Gradients",
}

ANGSTROM_TO_BOHR = 1.0 / 0.529177

# ══════════════════════════════════════════════════════════════════════════════
# 核心：單一 .npz → 多個 .cube
# ══════════════════════════════════════════════════════════════════════════════

def _write_cube_from_arrays(path, atomic_nums, atomic_coords,
                             grid_data, origin, axes, title="QSAR"):
    """寫出 Gaussian cube 格式（純函式，不依賴 qsar_grid_map.py）。"""
    A2B      = ANGSTROM_TO_BOHR
    nx,ny,nz = grid_data.shape
    n_atoms  = len(atomic_nums)
    ox,oy,oz = origin * A2B
    with open(path, "w") as f:
        f.write(f"QSAR CONTRIBUTION FIELD: {title}\n")
        f.write("Generated by npz_to_cube_batch.py\n")
        f.write(f"{n_atoms:5d} {ox:12.6f} {oy:12.6f} {oz:12.6f}\n")
        for i in range(3):
            f.write(f"{[nx,ny,nz][i]:5d} "
                    f"{axes[i,0]*A2B:12.6f} "
                    f"{axes[i,1]*A2B:12.6f} "
                    f"{axes[i,2]*A2B:12.6f}\n")
        for anum, (x,y,z) in zip(atomic_nums, atomic_coords):
            f.write(f"{int(anum):5d} {0.0:12.6f} "
                    f"{x*A2B:12.6f} {y*A2B:12.6f} {z*A2B:12.6f}\n")
        flat = grid_data.ravel()
        for i in range(0, len(flat), 6):
            f.write(" ".join(f"{v:13.5E}" for v in flat[i:i+6]) + "\n")


def convert_npz(npz_path: str, output_dir: str,
                fields: list | None = None) -> tuple:
    """
    將單一 .npz 轉換為指定場的 .cube 檔案。

    回傳 (mol_name, n_written, [error_str | None])
    Windows 安全：使用 mmap_mode=None + 明確 close()。
    """
    mol_name  = os.path.splitext(os.path.basename(npz_path))[0]
    written   = []

    try:
        # ── 讀取 npz（mmap_mode=None 避免 Windows handle 殘留）──────────────
        npzfile = np.load(npz_path, allow_pickle=True, mmap_mode=None)
        try:
            meta          = json.loads(str(npzfile["_meta"][0]))
            origin        = np.array(meta["origin"],  dtype=np.float64)
            axes          = np.array(meta["axes"],    dtype=np.float64)
            atomic_nums   = npzfile["atomic_nums"].astype(np.int32)
            atomic_coords = npzfile["atomic_coords"].astype(np.float64)

            field_map = {
                "saliency_total":   f"Saliency Total:{mol_name}",
                "saliency_electro": f"Saliency Electrostatic:{mol_name}",
                "saliency_steric":  f"Saliency Steric:{mol_name}",
                "perturb_steric":   f"Perturbation Steric:{mol_name}",
                "perturb_electro":  f"Perturbation Electro:{mol_name}",
                "ig_total":         f"IG:{mol_name}",
            }

            to_export = fields if fields else ALL_FIELDS
            # 把需要的場資料全部讀進記憶體
            field_data = {}
            for key in to_export:
                if key in npzfile:
                    field_data[key] = npzfile[key].astype(np.float64)
        finally:
            npzfile.close()   # 明確釋放 handle（Windows 必須）
            del npzfile
            gc.collect()

        # ── 寫出 .cube ────────────────────────────────────────────────────────
        os.makedirs(output_dir, exist_ok=True)
        for key, arr in field_data.items():
            cube_path = os.path.join(output_dir, f"{mol_name}_{key}.cube")
            _write_cube_from_arrays(
                cube_path, atomic_nums, atomic_coords,
                arr, origin, axes, title=field_map.get(key, key))
            written.append(cube_path)

        return mol_name, len(written), None

    except Exception:
        return mol_name, 0, traceback.format_exc(limit=4)


