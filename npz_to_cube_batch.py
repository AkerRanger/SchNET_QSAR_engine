# 批次轉換主函式
# ══════════════════════════════════════════════════════════════════════════════

def batch_convert(input_dir: str,
                  output_dir: str,
                  fields: list | None = None,
                  n_workers: int = 4,
                  resume: bool = True):
    """
    掃描 input_dir 下所有 .npz，批次轉換為 .cube。

    input_dir  : 含有 .npz 的目錄（qsar_grid_maps/）
    output_dir : .cube 輸出目錄（可與 input_dir 相同）
    fields     : 要轉換的場，None = 全部
    n_workers  : 平行 worker 執行緒數（.cube 寫入是 I/O bound，用 thread 即可）
    resume     : True = 跳過已存在的 .cube（斷點續轉）
    """
    # ── 收集 npz 清單 ─────────────────────────────────────────────────────────
    npz_files = sorted([
        os.path.join(input_dir, f)
        for f in os.listdir(input_dir)
        if f.lower().endswith(".npz") and not f.startswith("_")
    ])

    if not npz_files:
        print(_c(f"  找不到任何 .npz 檔案於：{input_dir}", "red"))
        return

    n_total = len(npz_files)
    print(f"  找到 {_c(str(n_total), 'bold')} 個 .npz 檔案")

    # ── 斷點續轉：跳過已有全部 .cube 的分子 ──────────────────────────────────
    to_convert = []
    skipped    = 0
    target_fields = fields if fields else ALL_FIELDS

    for npz_path in npz_files:
        mol_name = os.path.splitext(os.path.basename(npz_path))[0]
        if resume:
            # 只要有任何一個目標場的 .cube 不存在就納入轉換
            existing = [
                os.path.exists(os.path.join(output_dir, f"{mol_name}_{f}.cube"))
                for f in target_fields
            ]
            # 若 npz 實際包含的場數未知，保守策略：只要有任一缺少就轉換
            if all(existing):
                skipped += 1
                continue
        to_convert.append(npz_path)

    if skipped:
        print(_c(f"  ⚙  斷點續轉：跳過 {skipped} 個已完成分子", "cyan"))

    if not to_convert:
        print(_c("  所有分子均已轉換完成。", "green"))
        return

    print(f"  待轉換    : {_c(str(len(to_convert)), 'bold')} 個")
    print(f"  輸出欄位  : {_c(', '.join(target_fields), 'dim')}")
    print(f"  Workers   : {n_workers}")
    print(f"  輸出目錄  : {_c(output_dir, 'cyan')}")
    print()

    # ── 平行轉換（ThreadPoolExecutor，I/O bound 用 thread 即可）──────────────
    from concurrent.futures import ThreadPoolExecutor, as_completed

    progress   = _Progress(len(to_convert))
    fail_list  = []   # (mol_name, reason)
    success_list = []

    def _task(npz_path):
        mol_name, n_written, err = convert_npz(npz_path, output_dir, fields)
        progress.update(ok=(err is None))
        return mol_name, n_written, err

    with ThreadPoolExecutor(max_workers=n_workers) as pool:
        futures = {pool.submit(_task, p): p for p in to_convert}
        for fut in as_completed(futures):
            mol_name, n_written, err = fut.result()
            if err:
                fail_list.append((mol_name, err))
            else:
                success_list.append((mol_name, n_written))

    elapsed = progress.finish()

    # ── 結果摘要 ──────────────────────────────────────────────────────────────
    total_cubes = sum(n for _, n in success_list)
    print(_hr("═"))
    print(_c("  轉換完成", "bold", "green"))
    print(_hr())
    print(f"  成功   : {_c(str(len(success_list)), 'green')} 個分子  "
          f"（共 {_c(str(total_cubes), 'cyan')} 個 .cube 檔案）")
    print(f"  跳過   : {_c(str(skipped), 'cyan')} 個（已存在）")
    print(f"  失敗   : {_c(str(len(fail_list)), 'red'  if fail_list else 'dim')} 個")
    print(f"  耗時   : {elapsed:.1f}s")
    print(f"  輸出   : {_c(os.path.abspath(output_dir), 'cyan')}")

    if fail_list:
        print(_hr())
        print(_c("  失敗清單：", "red"))
        for mol_name, reason in fail_list:
            print(f"    {_c(mol_name, 'yellow')}")
            for line in reason.strip().splitlines()[-3:]:
                print(f"      {_c(line, 'dim')}")

    print(_hr("═"))

    # DS 操作提示
    print(f"\n  {_c('Discovery Studio 載入步驟', 'bold')}")
    print(_hr("─"))
    print(f"  1. File → Open → {_c('all_aligned.sdf', 'yellow')}（全部對齊分子）")
    print(f"  2. File → Open → 選擇想查看的 {_c('*_perturb_steric.cube', 'yellow')}")
    print(f"       右鍵 isosurface → 正等值 {_c('+2.0', 'green')} 綠色 / 負等值 {_c('-2.0', 'yellow')} 黃色")
    print(f"  3. File → Open → 選擇 {_c('*_perturb_electro.cube', 'yellow')}")
    print(f"       正等值 {_c('+2.0', 'cyan')} 藍色 / 負等值 {_c('-2.0', 'red')} 紅色")
    print()


