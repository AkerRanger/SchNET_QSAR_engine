def _prompt(label, default=None):
    hint = f"  [{_c(str(default), 'dim')}]" if default is not None else ""
    raw = input(f"  {_c('▸', 'cyan')} {label}{hint}: ").strip()
    return raw if raw else (str(default) if default is not None else "")

def _prompt_int(label, default, lo=1, hi=64):
    while True:
        raw = _prompt(label, default)
        try:
            v = int(raw)
            if lo <= v <= hi: return v
            print(_c(f"    請輸入 {lo}–{hi}", "red"))
        except ValueError:
            print(_c("    請輸入整數", "red"))

def _confirm(label, default=True):
    hint = "Y/n" if default else "y/N"
    raw = input(f"  {_c('▸', 'cyan')} {label} [{hint}]: ").strip().lower()
    return default if not raw else raw in ("y", "yes", "是")

def _print_banner():
    print("\n" + _hr("═"))
    print(_c("  NPZ → CUBE 批次轉換工具", "bold", "cyan"))
    print(_c("  npz_to_cube_batch.py  ·  qsar_grid_map.py 配套工具", "dim"))
    print(_hr("═") + "\n")


