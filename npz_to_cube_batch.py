# 主程式
# ══════════════════════════════════════════════════════════════════════════════

def main():
    if sys.platform == "win32":
        os.system("")

    parser = argparse.ArgumentParser(
        description="批次將 .npz 轉換為 .cube",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__)
    parser.add_argument("--input",   "-i", help="含 .npz 的目錄")
    parser.add_argument("--output",  "-o", help=".cube 輸出目錄（預設與 input 相同）")
    parser.add_argument("--fields",  "-f", nargs="+", choices=ALL_FIELDS,
                        help="要轉換的場（預設全部）")
    parser.add_argument("--workers", "-w", type=int, default=None,
                        help="平行 worker 數（預設 CPU 核心數的一半）")
    parser.add_argument("--no-resume", action="store_true",
                        help="不跳過已存在的 .cube，強制重新轉換")
    args = parser.parse_args()

    _print_banner()

    # ── 互動式補全 ────────────────────────────────────────────────────────────
    if sys.platform == "win32":
        os.system("")

    # input_dir
    if args.input:
        input_dir = args.input
    else:
        print(_c("  Step 1 / 3  ·  輸入目錄", "bold"))
        print(_hr())
        while True:
            input_dir = _prompt("含 .npz 的目錄", default="qsar_grid_maps")
            if os.path.isdir(input_dir): break
            print(_c(f"    找不到目錄：{input_dir}", "red"))

    # output_dir
    if args.output:
        output_dir = args.output
    else:
        print(f"\n  {_c('Step 2 / 3  ·  輸出目錄', 'bold')}")
        print(_hr())
        print(_c("  （直接 Enter = 與輸入目錄相同，.cube 與 .npz 放在一起）", "dim"))
        raw_out = _prompt("輸出目錄", default=input_dir)
        output_dir = raw_out if raw_out else input_dir

    # 場選擇
    if args.fields:
        fields = args.fields
    else:
        print(f"\n  {_c('Step 3 / 3  ·  選擇要轉換的場', 'bold')}")
        print(_hr())
        print(_c("  （直接 Enter = 全部轉換）", "dim"))
        for i, key in enumerate(ALL_FIELDS, 1):
            print(f"    {_c(str(i), 'cyan')}.  {_c(key, 'white'):<22}  "
                  f"{_c(FIELD_LABELS[key], 'dim')}")
        raw_fields = _prompt("輸入場編號（空白分隔，Enter=全部）", default="")
        if raw_fields.strip():
            chosen = []
            for token in raw_fields.split():
                try:
                    idx = int(token) - 1
                    if 0 <= idx < len(ALL_FIELDS):
                        chosen.append(ALL_FIELDS[idx])
                    else:
                        print(_c(f"  無效編號 {token}，忽略", "yellow"))
                except ValueError:
                    if token in ALL_FIELDS:
                        chosen.append(token)
                    else:
                        print(_c(f"  未知場名 {token}，忽略", "yellow"))
            fields = chosen if chosen else None
        else:
            fields = None

    # workers
    if args.workers:
        n_workers = args.workers
    else:
        cpu_avail = multiprocessing.cpu_count()
        n_workers = min(cpu_avail, 8)   # I/O bound，8 個通常足夠

    resume = not args.no_resume

    # ── 確認並執行 ────────────────────────────────────────────────────────────
    print()
    print(_hr())
    print(f"  輸入目錄  : {_c(os.path.abspath(input_dir), 'cyan')}")
    print(f"  輸出目錄  : {_c(os.path.abspath(output_dir), 'cyan')}")
    print(f"  轉換欄位  : {_c(', '.join(fields) if fields else '全部', 'green')}")
    print(f"  Workers   : {n_workers}")
    print(f"  斷點續轉  : {'是' if resume else '否（強制重轉）'}")
    print(_hr())

    if not _confirm("開始轉換？", default=True):
        print(_c("  已取消。", "dim"))
        sys.exit(0)

    print()
    batch_convert(
        input_dir  = input_dir,
        output_dir = output_dir,
        fields     = fields,
        n_workers  = n_workers,
        resume     = resume,
    )


if __name__ == "__main__":
    main()
